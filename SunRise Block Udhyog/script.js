function applyLang(lang){
  document.documentElement.lang = (lang === "np") ? "ne" : "en";
  document.querySelectorAll("[data-i18n]").forEach(el=>{
    const key = el.getAttribute("data-i18n");
    const val = i18n?.[lang]?.[key];
    if (val) el.textContent = val;
  });

  // toggle pill
  const pill = document.getElementById("langPill");
  if (pill) pill.textContent = (lang === "np") ? "NP" : "EN";
  
  const ghost = document.querySelector(".lang__ghost");
if (ghost) ghost.textContent = (lang === "np") ? "EN" : "NP";

  // store
  localStorage.setItem("sbu_lang", lang);
}

function initLang(){
  const saved = localStorage.getItem("sbu_lang");
  const lang = (saved === "np" || saved === "en") ? saved : "en";
  applyLang(lang);

  const toggle = document.getElementById("langToggle");
  toggle?.addEventListener("click", ()=>{
    const current = localStorage.getItem("sbu_lang") || "en";
    applyLang(current === "en" ? "np" : "en");
  });
}

function initReveal(){
  const els = document.querySelectorAll(".reveal");
  const io = new IntersectionObserver((entries)=>{
    entries.forEach(e=>{
      if (e.isIntersecting){
        const delay = e.target.getAttribute("data-delay");
        if (delay){
          e.target.style.transitionDelay = `${delay}ms`;
        }
        e.target.classList.add("is-in");
        io.unobserve(e.target);
      }
    });
  }, {threshold: 0.16});
  els.forEach(el=>io.observe(el));
}

function initTopbar(){
  const topbar = document.getElementById("topbar");
  const onScroll = ()=>{
    if (window.scrollY > 18) topbar.classList.add("is-solid");
    else topbar.classList.remove("is-solid");
  };
  onScroll();
  window.addEventListener("scroll", onScroll, {passive:true});
}

document.getElementById("year").textContent = new Date().getFullYear();

initLang();
initReveal();
initTopbar();
